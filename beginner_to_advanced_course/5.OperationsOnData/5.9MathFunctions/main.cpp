#include <iostream>
#include <cmath>

int main()
{
  std::cout << std::sqrt(16) << std::endl;
  std::cout << std::pow(2, 3) << std::endl;
  std::cout << std::abs(-5) << std::endl;
  std::cout << std::ceil(3.2) << std::endl;
  std::cout << std::floor(3.8) << std::endl;
  std::cout << std::round(3.5) << std::endl;
  std::cout << std::exp(10) << std::endl;
  std::cout << std::log(10) << std::endl;
  std::cout << std::sin(0) << std::endl;
  std::cout << std::cos(0) << std::endl;
  std::cout << std::tan(0) << std::endl;
  
  return 0;
}